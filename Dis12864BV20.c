void setDisplayExtendedFunction(uint8_t bit_interface_control, uint8_t  extended_instruction_set_control_bit, uint8_t Graphic_Display_bit) 
/*Установка режима работы дисплея.
	Если extended_instruction_set_control_bit=0 - базовый набор инструкций.
	Если extended_instruction_set_control_bit!=0 - расширенный набор инструкций.
	Если Graphic_Display_bit=0 - графический режим отключен.
	Если Graphic_Display_bit!=0 - графический режим включен.*/
{	
	uint8_t a=0x20;
	if (bit_interface_control>0)
	a=a|(1<<4);
	if (extended_instruction_set_control_bit>0)
	a=a|(1<<2);
	if (Graphic_Display_bit>0)
	a=a|(1<<1);
	transferDisplayByte(a,COMMAND);
}
