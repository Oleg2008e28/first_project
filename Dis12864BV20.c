void controlDisplay(uint8_t Display_ON_OFF_control_bit, uint8_t Cursor_ON_OFF_control_bit, uint8_t Character_Blink_ON_OFF_control_bit) 
{	
	/*Если Display_ON_OFF_control_bit=0 - дисплей выключается.
	  Функция проверена, работает полностью. */
	uint8_t a=0x08;
	if (Display_ON_OFF_control_bit>0)
		a=a|(1<<2);
	if (Cursor_ON_OFF_control_bit>0)
		a=a|(1<<1);	
	if (Character_Blink_ON_OFF_control_bit>0)
		a=a|(1<<0);
	transferDisplayByte(a,COMMAND);
}
